/// Glyph table index for `c`; unmapped characters fall back to '?'.
fn glyph_index(c: char) -> usize {
    let code = c as u32;
    if (32..=126).contains(&code) {
        (code - 32) as usize
    } else {
        ('?' as usize) - 32
    }
}

/// Pixel origin (top-left) of glyph cell `i` in the atlas.
fn cell_origin(i: usize) -> (u32, u32) {
    let col = i as u32 % CELLS_PER_ROW;
    let row = i as u32 / CELLS_PER_ROW;
    (col * GLYPH_SIZE, row * GLYPH_SIZE)
}

/// Builds the R8 font atlas: `ATLAS_WIDTH * ATLAS_HEIGHT` bytes, row-major,
/// 255 where a pixel is set and 0 elsewhere. Rows 0..6 hold ASCII 32..126;
/// the cell at (0, 56)..(8, 64) is solid white for untextured 2D draws.
pub fn build_atlas() -> Vec<u8> {
    let mut atlas = vec![0u8; (ATLAS_WIDTH * ATLAS_HEIGHT) as usize];
    for (i, glyph) in GLYPHS.iter().enumerate() {
        let (px, py) = cell_origin(i);
        for (y, row_byte) in glyph.iter().enumerate() {
            for x in 0..GLYPH_SIZE {
                // LSB-first: bit x is the pixel x columns from the left.
                if row_byte >> x & 1 != 0 {
                    let idx = (py + y as u32) * ATLAS_WIDTH + px + x;
                    atlas[idx as usize] = 255;
                }
            }
        }
    }
    // Solid white cell.
    for y in WHITE_CELL_Y..WHITE_CELL_Y + GLYPH_SIZE {
        for x in WHITE_CELL_X..WHITE_CELL_X + GLYPH_SIZE {
            atlas[(y * ATLAS_WIDTH + x) as usize] = 255;
        }
    }
    atlas
}

/// Returns `(uv_min, uv_max)` of the glyph cell for `c` (unmapped chars use
/// '?'). UVs are inset half a texel from the cell edges so NEAREST sampling
/// never reads a neighboring cell.
pub fn glyph_uv(c: char) -> ([f32; 2], [f32; 2]) {
    let (px, py) = cell_origin(glyph_index(c));
    let w = ATLAS_WIDTH as f32;
    let h = ATLAS_HEIGHT as f32;
    let uv_min = [(px as f32 + 0.5) / w, (py as f32 + 0.5) / h];
    let uv_max = [(px as f32 + 7.5) / w, (py as f32 + 7.5) / h];
    (uv_min, uv_max)
}

/// UV of the center of the solid white cell; solid 2D shapes sample this.
pub fn white_uv() -> [f32; 2] {
    [
        (WHITE_CELL_X as f32 + GLYPH_SIZE as f32 / 2.0) / ATLAS_WIDTH as f32,
        (WHITE_CELL_Y as f32 + GLYPH_SIZE as f32 / 2.0) / ATLAS_HEIGHT as f32,
    ]
}

/// Pixel width of `text` at `font_size`: glyphs draw as font_size x font_size
/// squares, so width is simply the char count times the size.
pub fn measure_text(text: &str, font_size: i32) -> i32 {
    text.chars().count() as i32 * font_size
}

#[cfg(test)]
mod tests {
    use super::*;

    /// Extracts an 8x8 cell from the atlas as row-major bytes.
    fn atlas_cell(atlas: &[u8], px: u32, py: u32) -> Vec<u8> {
        let mut out = Vec::with_capacity(64);
        for y in 0..GLYPH_SIZE {
            for x in 0..GLYPH_SIZE {
                out.push(atlas[((py + y) * ATLAS_WIDTH + px + x) as usize]);
            }
        }
        out
    }

    /// Renders an 8x8 cell of atlas bytes to 8 lines of '#'/'.'.
    fn render_cell(cell: &[u8]) -> String {
        cell.chunks(8)
            .map(|row| {
                row.iter()
                    .map(|&b| if b != 0 { '#' } else { '.' })
                    .collect::<String>()
            })
            .collect::<Vec<_>>()
            .join("\n")
    }

    #[test]
    fn atlas_has_correct_length() {
        assert_eq!(build_atlas().len(), (ATLAS_WIDTH * ATLAS_HEIGHT) as usize);
    }

    #[test]
    fn space_cell_blank_and_a_cell_not() {
        let atlas = build_atlas();
        // Space is index 0 -> cell (0, 0).
        let space = atlas_cell(&atlas, 0, 0);
        assert!(space.iter().all(|&b| b == 0), "space cell must be blank");
        // 'A' is index 33 -> cell (col 1, row 2) -> pixels (8, 16).
        let a = atlas_cell(&atlas, 8, 16);
        assert!(a.iter().any(|&b| b == 255), "'A' cell must have set pixels");
    }

    #[test]
    fn white_cell_all_255() {
        let atlas = build_atlas();
        let white = atlas_cell(&atlas, WHITE_CELL_X, WHITE_CELL_Y);
        assert!(white.iter().all(|&b| b == 255));
    }

    #[test]
    fn measure_text_scales_by_char_count() {
        assert_eq!(measure_text("abc", 20), 60);
    }

    #[test]
    fn glyph_uv_a_within_cell_bounds() {
        let (uv_min, uv_max) = glyph_uv('A');
        // Independently computed: 'A' = 65, i = 33, cell (1, 2), px 8, py 16.
        let (u0, u1) = (8.0 / 128.0, 16.0 / 128.0);
        let (v0, v1) = (16.0 / 64.0, 24.0 / 64.0);
        for uv in [uv_min, uv_max] {
            assert!(uv[0] >= 0.0 && uv[0] <= 1.0);
            assert!(uv[1] >= 0.0 && uv[1] <= 1.0);
            assert!(uv[0] >= u0 && uv[0] <= u1);
            assert!(uv[1] >= v0 && uv[1] <= v1);
        }
        assert!(uv_min[0] < uv_max[0] && uv_min[1] < uv_max[1]);
    }

    #[test]
    fn atlas_glyph_matches_const_table() {
        let atlas = build_atlas();
        let from_atlas = render_cell(&atlas_cell(&atlas, 8, 16));
        // Same rendering straight from the const table (LSB = leftmost).
        let glyph = &GLYPHS[glyph_index('A')];
        let from_table = glyph
            .iter()
            .map(|row_byte| {
                (0..8)
                    .map(|x| if row_byte >> x & 1 != 0 { '#' } else { '.' })
                    .collect::<String>()
            })
            .collect::<Vec<_>>()
            .join("\n");
        assert_eq!(from_atlas, from_table);
    }
}
