//! Embedded 8x8 bitmap font and CPU-side atlas builder for the 2D text overlay.
//!
//! Glyph bitmaps are from `font8x8` (font8x8_basic) by Daniel Hepper
//! <daniel@hepper.net>, based on public-domain IBM VGA fonts collected by
//! Marcel Sondaar. License: Public Domain. The tables below cover ASCII
//! 32..=126; each glyph is 8 bytes, one per row top-to-bottom, and the LSB of
//! each row byte is the LEFTMOST pixel.
//!
//! `build_atlas` packs the glyphs into a single R8 texture (16 cells per row)
//! plus one guaranteed solid-white cell that untextured 2D shapes sample.
//! The atlas is sampled with NEAREST filtering, so glyph UVs are inset by half
//! a texel on every side to avoid bleeding from neighboring cells.

